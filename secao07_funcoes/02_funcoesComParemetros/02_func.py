# Exemplo 2
def soma(num1, num2):
    print(num1 + num2)

def subtracao(num1, num2):
    print(num1 - num2)

def divisao(num1, num2):
    print(num1 / num2)

def multiplicacao(num1, num2):
    print(num1 * num2)

n1 = int(input('Primeiro numero: '))
n2 = int(input('Segundo numero: '))

soma(n1, n2)
subtracao(n1, n2)
divisao(n1, n2)
multiplicacao(n1, n2)

